//! C header generation - migration step 3 (DESIGN.md §6).
//!
//! Generates the *data* halves of the analyzer's tables from database/:
//!
//!   analyzer/lookup.h                  - whole file (it is all data)
//!   analyzer/physicalquantity-data.h   - PhysicalQuantity statics + list
//!   analyzer/fieldtype-data.h          - fieldTypeList[]
//!   analyzer/pgn-data.h                - pgnList[]
//!
//! The hand-written halves (structs, macros, prototypes) stay in pgn.h /
//! fieldtype.h, which #include the data files inside their GLOBALS blocks.
//! pgn-j1939.h is untouched (self-contained macro set, hand-written data).
//!
//! Emission philosophy: pure designated initializers, no field macro zoo.
//! Values are emitted authored-or-default exactly as the pgn.h macros would
//! have set them, so fillFieldType() resolves to identical runtime tables -
//! proven by the step-3 gate: the OLD C XML path (analyzer-explain) over the
//! generated headers must reproduce docs/canboat.xml byte-for-byte, and the
//! analyzer test suite must pass.

use crate::derive::default_field_resolution;
use crate::model::{Database, Field, FieldType, Interval, Pgn};

const BANNER: &str = "/* GENERATED BY keel FROM database/ - DO NOT EDIT.\n\
 *\n\
 * (C) 2009-2026, Kees Verruijt, Harlingen, The Netherlands.\n\
 * Part of CANboat; licensed under the Apache License, Version 2.0.\n\
 */\n\n";

fn c_str(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for c in s.chars() {
        match c {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            '\n' => out.push_str("\\n"),
            _ => out.push(c),
        }
    }
    out.push('"');
    out
}

/// Shortest round-trip double literal; C parses it back to the same f64.
fn c_double(v: f64) -> String {
    format!("{v:?}")
}

fn sentinel_symbol(name: &str) -> &'static str {
    match name {
        "TopOfRange" => "SENTINEL_TOP_OF_RANGE",
        "NaN" => "SENTINEL_NAN",
        "EmptyString" => "SENTINEL_EMPTY_STRING",
        "Variable" => "SENTINEL_VARIABLE",
        _ => "SENTINEL_NONE",
    }
}

