/// Emitted from the AUTHORED fieldtype attributes (pre-percolation), exactly
/// like the hand-written initializers: fillFieldType() re-derives the rest.
pub fn emit_fieldtype_data_h(authored: &[FieldType]) -> String {
    let mut out = String::with_capacity(64 << 10);
    out.push_str(BANNER);
    out.push_str("FieldType fieldTypeList[] = {\n");
    for ft in authored {
        out.push_str("    {");
        let mut parts: Vec<String> = vec![format!(".name = {}", c_str(&ft.name))];
        if let Some(v) = &ft.description {
            parts.push(format!(".description = {}", c_str(v)));
        }
        if let Some(v) = &ft.encoding_description {
            parts.push(format!(".encodingDescription = {}", c_str(v)));
        }
        if let Some(v) = &ft.comment {
            parts.push(format!(".comment = {}", c_str(v)));
        }
        if let Some(v) = &ft.url {
            parts.push(format!(".url = {}", c_str(v)));
        }
        if ft.size != 0 {
            parts.push(format!(".size = {}", ft.size));
        }
        if ft.variable_size {
            parts.push(".variableSize = True".into());
        }
        if let Some(v) = &ft.base {
            parts.push(format!(".baseFieldType = {}", c_str(v)));
        }
        if let Some(v) = &ft.unit {
            parts.push(format!(".unit = {}", c_str(v)));
        }
        if ft.offset != 0 {
            parts.push(format!(".offset = {}", ft.offset));
        }
        if ft.resolution != 0.0 {
            parts.push(format!(".resolution = {}", c_double(ft.resolution)));
        }
        if let Some(s) = ft.has_sign {
            parts.push(format!(".hasSign = {}", if s { "True" } else { "False" }));
        }
        if ft.sentinels != "None" {
            parts.push(format!(".sentinels = {}", sentinel_symbol(&ft.sentinels)));
        }
        if let Some(v) = &ft.physical {
            parts.push(format!(".physical = &{v}"));
        }
        if let Some(v) = &ft.print_function {
            parts.push(format!(".pf = {v}"));
        }
        if let Some(v) = ft.range_min_authored {
            parts.push(format!(".rangeMin = {}", c_double(v)));
        }
        if let Some(v) = ft.range_max_authored {
            parts.push(format!(".rangeMax = {}", c_double(v)));
        }
        out.push_str(&parts.join(",\n     "));
        out.push_str("},\n\n");
    }
    out.push_str("};\n");
    out
}

