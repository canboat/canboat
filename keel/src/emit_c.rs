pub fn emit_physicalquantity_data_h(db: &Database) -> String {
    let mut out = String::with_capacity(16 << 10);
    out.push_str(BANNER);
    for pq in &db.physical_quantities {
        out.push_str(&format!("static const PhysicalQuantity {} = {{\n", pq.name));
        out.push_str(&format!("    .name = {},\n", c_str(&pq.name)));
        if let Some(v) = &pq.description {
            out.push_str(&format!("    .description = {},\n", c_str(v)));
        }
        if let Some(v) = &pq.comment {
            out.push_str(&format!("    .comment = {},\n", c_str(v)));
        }
        if let Some(v) = &pq.unit {
            out.push_str(&format!("    .abbreviation = {},\n", c_str(v)));
        }
        if let Some(v) = &pq.unit_description {
            out.push_str(&format!("    .unit = {},\n", c_str(v)));
        }
        if let Some(v) = &pq.url {
            out.push_str(&format!("    .url = {},\n", c_str(v)));
        }
        out.push_str("};\n\n");
    }
    out.push_str("const PhysicalQuantity *const PhysicalQuantityList[] = {\n");
    for pq in &db.physical_quantities {
        out.push_str(&format!("    &{},\n", pq.name));
    }
    out.push_str("    NULL,\n};\n");
    out
}

