const LOOKUP_PROLOGUE: &str = r#"
#ifndef LOOKUP_TYPE
#define LOOKUP_TYPE(a, b)
#endif
#ifndef LOOKUP_TYPE_TRIPLET
#define LOOKUP_TYPE_TRIPLET(a, b)
#endif
#ifndef LOOKUP_TYPE_BITFIELD
#define LOOKUP_TYPE_BITFIELD(a, b)
#endif
#ifndef LOOKUP_TYPE_FIELDTYPE
#define LOOKUP_TYPE_FIELDTYPE(a, b)
#endif
#ifndef LOOKUP
#define LOOKUP(a, b, c)
#endif
#ifndef LOOKUP_TRIPLET
#define LOOKUP_TRIPLET(a, b, c, d)
#endif
#ifndef LOOKUP_BITFIELD
#define LOOKUP_BITFIELD(a, b, c)
#endif
#ifndef LOOKUP_FIELDTYPE
#define LOOKUP_FIELDTYPE(a, b, c, d)
#endif
#ifndef LOOKUP_FIELDTYPE_LOOKUP
#define LOOKUP_FIELDTYPE_LOOKUP(a, b, c, d, e, f, g)
#endif
#ifndef LOOKUP_END
#define LOOKUP_END
#endif
"#;

const LOOKUP_EPILOGUE: &str = "// Keep this at the end, so a next include does not need to do this
#undef LOOKUP_TYPE
#undef LOOKUP_TYPE_TRIPLET
#undef LOOKUP_TYPE_BITFIELD
#undef LOOKUP_TYPE_FIELDTYPE
#undef LOOKUP
#undef LOOKUP_TRIPLET
#undef LOOKUP_BITFIELD
#undef LOOKUP_FIELDTYPE
#undef LOOKUP_FIELDTYPE_LOOKUP
#undef LOOKUP_END
";

pub fn emit_lookup_h(db: &Database) -> String {
    let mut out = String::with_capacity(256 << 10);
    out.push_str(BANNER);
    out.push_str(LOOKUP_PROLOGUE);
    out.push('\n');

    for lk in db.ordered_lookups("pair") {
        out.push_str(&format!("LOOKUP_TYPE({}, BITS({}))\n", lk.name, lk.bits));
        for (value, name) in &lk.pairs {
            out.push_str(&format!("LOOKUP({}, {value}, {})\n", lk.name, c_str(name)));
        }
        out.push_str("LOOKUP_END\n\n");
    }
    for lk in db.ordered_lookups("triplet") {
        out.push_str(&format!(
            "LOOKUP_TYPE_TRIPLET({}, BITS({}))\n",
            lk.name, lk.bits
        ));
        for (v1, v2, name) in &lk.triplets {
            out.push_str(&format!(
                "LOOKUP_TRIPLET({}, {v1}, {v2}, {})\n",
                lk.name,
                c_str(name)
            ));
        }
        out.push_str("LOOKUP_END\n\n");
    }
    for lk in db.ordered_lookups("bit") {
        out.push_str(&format!(
            "LOOKUP_TYPE_BITFIELD({}, BITS({}))\n",
            lk.name, lk.bits
        ));
        for (bit, name) in &lk.pairs {
            out.push_str(&format!(
                "LOOKUP_BITFIELD({}, {bit}, {})\n",
                lk.name,
                c_str(name)
            ));
        }
        out.push_str("LOOKUP_END\n\n");
    }
    for lk in db.ordered_lookups("fieldtype") {
        out.push_str(&format!(
            "LOOKUP_TYPE_FIELDTYPE({}, BITS({}))\n",
            lk.name, lk.bits
        ));
        for e in &lk.fieldtypes {
            let ft = &db.fieldtypes[db.fieldtype(&e.fieldtype).expect("checked")];
            match &e.lookup {
                Some(nested) => {
                    let bits = e.bits.unwrap_or(ft.size);
                    let kind = e.lookup_kind.as_deref().unwrap_or("pair").to_uppercase();
                    out.push_str(&format!(
                        "LOOKUP_FIELDTYPE_LOOKUP({}, {}, {}, {}, {bits}, {kind}, {nested})\n",
                        lk.name,
                        e.value,
                        c_str(&e.name),
                        c_str(&e.fieldtype)
                    ));
                }
                None => {
                    out.push_str(&format!(
                        "LOOKUP_FIELDTYPE({}, {}, {}, {})\n",
                        lk.name,
                        e.value,
                        c_str(&e.name),
                        c_str(&e.fieldtype)
                    ));
                }
            }
        }
        out.push_str("LOOKUP_END\n\n");
    }

    out.push_str(LOOKUP_EPILOGUE);
    out
}

