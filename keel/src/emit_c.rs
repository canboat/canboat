fn complete_expr(pgn: &Pgn) -> String {
    // The Interval bit is derived at runtime (fieldtype.c:486), never emitted.
    let mut bits: Vec<&str> = Vec::new();
    for m in &pgn.missing {
        bits.push(match m.as_str() {
            "Fields" => "PACKET_FIELDS_UNKNOWN",
            "FieldLengths" => "PACKET_FIELD_LENGTHS_UNKNOWN",
            "Resolution" => "PACKET_RESOLUTION_UNKNOWN",
            "Lookups" => "PACKET_LOOKUPS_UNKNOWN",
            "SampleData" => "PACKET_NOT_SEEN",
            "MissingCompanyFields" => "PACKET_MISSING_COMPANY_FIELDS",
            _ => continue,
        });
    }
    if bits.is_empty() {
        "PACKET_COMPLETE".into()
    } else {
        bits.join(" | ")
    }
}

fn packet_type_symbol(t: &str) -> &'static str {
    match t {
        "Fast" => "PACKET_FAST",
        "ISO" => "PACKET_ISO_TP",
        "Mixed" => "PACKET_MIXED",
        _ => "PACKET_SINGLE",
    }
}

fn emit_field(db: &Database, f: &Field) -> String {
    let ft = &db.fieldtypes[f.ft];
    // camelName (the frozen <Id>) is explicit on every field, like the YAML;
    // analyzer.c keys naming decisions on the showCamel mode, never on
    // camelName presence.
    let mut parts: Vec<String> = vec![
        format!(".name = {}", c_str(&f.name)),
        format!(".camelName = {}", c_str(&f.id)),
        format!(".fieldType = {}", c_str(&f.type_)),
    ];
    if let Some(bits) = f.bits {
        parts.push(format!(".size = {bits}"));
    }
    // resolution: what the pgn.h macro would have set - authored, or the
    // per-root default when the fieldtype carries no resolution of its own
    // (fillFieldType inherits the fieldtype's when we emit nothing).
    if let Some(r) = f.resolution {
        parts.push(format!(".resolution = {}", c_double(r)));
    } else if ft.resolution == 0.0 {
        let d = default_field_resolution(&ft.root_name);
        if d != 0.0 {
            parts.push(format!(".resolution = {}", c_double(d)));
        }
    }
    if ft.has_sign == Some(true) {
        parts.push(".hasSign = true".into());
    }
    if let Some(m) = &f.match_ {
        parts.push(format!(".unit = {}", c_str(&format!("={m}"))));
    } else if let Some(u) = &f.unit {
        parts.push(format!(".unit = {}", c_str(u)));
    }
    if let Some(o) = f.offset {
        parts.push(format!(".offset = {o}"));
    }
    if let Some(d) = &f.description {
        parts.push(format!(".description = {}", c_str(d)));
    }
    if let Some((kind, name)) = f.lookup_ref() {
        let (type_sym, member) = match kind {
            "triplet" => ("LOOKUP_TYPE_TRIPLET", "LOOKUP_TRIPLET_MEMBER"),
            "bit" => ("LOOKUP_TYPE_BIT", "LOOKUP_BIT_MEMBER"),
            "fieldtype" => ("LOOKUP_TYPE_FIELDTYPE", "LOOKUP_FIELDTYPE_MEMBER"),
            _ => ("LOOKUP_TYPE_PAIR", "LOOKUP_PAIR_MEMBER"),
        };
        parts.push(format!(".lookup.type = {type_sym}"));
        parts.push(format!("{member} = lookup{name}"));
        parts.push(format!(".lookup.name = {}", c_str(name)));
        if let Some(order) = f.lookup_indirect_order {
            parts.push(format!(".lookup.val1Order = {order}"));
        }
    } else if ft.root_name == "LOOKUP" {
        // UNKNOWN_LOOKUP_FIELD: lookup type without an enumeration
        parts.push(".lookup.type = LOOKUP_TYPE_PAIR".into());
    }
    if f.proprietary {
        parts.push(".proprietary = true".into());
    }
    if f.primary_key {
        parts.push(".partOfPrimaryKey = true".into());
    }
    if let Some(sv) = f.special_values {
        parts.push(format!(".reservedOverride = {}", sv + 1));
    }
    if f.dynamic_field_length {
        parts.push(".dynamicFieldLength = true".into());
        if f.dynamic_field_length_overhead != 0 {
            parts.push(format!(
                ".dynamicFieldLengthOverhead = {}",
                f.dynamic_field_length_overhead
            ));
        }
    }
    if let Some(v) = f.range_min {
        parts.push(format!(".rangeMin = {}", c_double(v)));
    }
    if let Some(v) = f.range_max {
        parts.push(format!(".rangeMax = {}", c_double(v)));
    }
    format!("      {{{}}}", parts.join(", "))
}

fn emit_pgn(db: &Database, p: &Pgn) -> String {
    let mut out = String::with_capacity(2 << 10);
    out.push_str(&format!(
        "    {{{},\n     {},\n     {},\n     {},\n     {{\n",
        c_str(&p.description),
        p.pgn,
        complete_expr(p),
        packet_type_symbol(&p.type_)
    ));
    let fields: Vec<String> = p.fields.iter().map(|f| emit_field(db, f)).collect();
    out.push_str(&fields.join(",\n"));
    out.push_str("\n     },\n");

    let mut named: Vec<String> = vec![format!(".camelDescription = {}", c_str(&p.id))];
    if p.fallback {
        named.push(".fallback = true".into());
    }
    match p.interval {
        Interval::Ms(ms) if ms != 0 => named.push(format!(".interval = {ms}")),
        Interval::Irregular => named.push(".interval = UINT16_MAX".into()),
        _ => {}
    }
    if p.priority != 0 {
        named.push(format!(".priority = {}", p.priority));
    }
    for (n, rep) in [(1, &p.repeating1), (2, &p.repeating2)] {
        if let Some(rep) = rep {
            if rep.count > 0 {
                named.push(format!(".repeatingCount{n} = {}", rep.count));
                named.push(format!(".repeatingStart{n} = {}", rep.start));
                named.push(format!(
                    ".repeatingField{n} = {}",
                    rep.count_field.unwrap_or(255)
                ));
            }
        }
    }
    if let Some(v) = &p.explanation {
        named.push(format!(".explanation = {}", c_str(v)));
    }
    if let Some(v) = &p.url {
        named.push(format!(".url = {}", c_str(v)));
    }
    if let Some(v) = &p.research_doc {
        named.push(format!(".researchDoc = {}", c_str(v)));
    }
    out.push_str(&format!("     {}}}", named.join(",\n     ")));
    out
}

pub fn emit_pgn_data_h(db: &Database, j1939: bool) -> String {
    let list = if j1939 { &db.pgns_j1939 } else { &db.pgns };
    let mut out = String::with_capacity(2 << 20);
    out.push_str(BANNER);
    out.push_str("Pgn pgnList[] = {\n");
    let pgns: Vec<String> = list.iter().map(|p| emit_pgn(db, p)).collect();
    out.push_str(&pgns.join(",\n\n"));
    out.push_str("\n};\n");
    out
}
